public class Enemy {
    private float health;
    private int damage;

    public Enemy(float health, int damage) {
        this.health = health;
        this.damage = damage;
    }

    public float getHealth() {
        return health;
    }

    public void setHealth(float health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }
}